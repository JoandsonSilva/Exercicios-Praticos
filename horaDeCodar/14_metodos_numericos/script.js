// parseFloat converte para números decimais

console.log(parseFloat('12.2'))
console.log(Number.parseFloat('12.2'))

// parseInt converte para números inteiros

console.log(parseInt('10'))
console.log(parseInt(12.9))

// toFixed determina quantidade de casas decimais, com posibilidade de aredondamento

console.log(5.49999.toFixed(1))
console.log(6.49999.toFixed(2))

//isNaN verifica se é número

console.log(isNaN(1))
console.log(isNaN('15'))
console.log(isNaN('M'))

//MAX_VALUE e MIN_VALUE tamanho de trabalho de um número em JavaScript

console.log(Number.MAX_VALUE)
console.log(Number.MIN_VALUE)
